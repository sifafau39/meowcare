    <p>Kucing dalam bahasa latin adalah Felis Silvestris Catus. Kucing adalah jenis hewan mamalia karnivora yang berasal dari keluarga Felidae. Habitat kucing adalah di darat yang biasanya berbaur dengan manusia sebagai peliharaan dan ada juga yang hidup liar. Istilah kucing ini biasanya tertuju pada hewan kucing kecil yang telah jinak dan dipelihara manusia tetapi sebenarnya istilah kucing juga bisa merujuk kepada kucing besar seperti singa dan harimau.
    Kucing sangat rentan terhadap penyakit jika tidak dirawat dengan baik, penyakit kucing sering disebabkan oleh virus, parasit atau bakteri yang menempel pada tubuh kucing tanpa sepengetahuan pemilik kucing. Oleh karena itu para pemilik kucing harus selalu menyanyangi dan terus memperhatikan kesehatannya, agar kucing kesayangan tidak terkena penyakit yang bisa saja mengancam nyawanya.</p>

